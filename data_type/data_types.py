# Integer type
number_of_students = 30
print(f"Integer: {number_of_students}")

# Float type
average_temperature = 23.5
print(f"Float: {average_temperature}")

# String type
course_name = "Introduction to Python"
print(f"String: {course_name}")

# Boolean type
is_course_active = True
print(f"Boolean: {is_course_active}")

# List type
student_names = ["Alice", "Bob", "Charlie"]
print(f"List: {student_names}")

# Tuple type
coordinates = (45.4215, -75.6972)
print(f"Tuple: {coordinates}")

# Set type
unique_subjects = {"Math", "Science", "History"}
print(f"Set: {unique_subjects}")

# Dictionary type
student_scores = {"Alice": 85, "Bob": 90, "Charlie": 78}
print(f"Dictionary: {student_scores}")

# None type
optional_field = None
print(f"None: {optional_field}")

# Complex number type
complex_number = 3 + 4j
print(f"Complex: {complex_number}")

